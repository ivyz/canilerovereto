              La sigla PAN-EPPAA &egrave; un acronimo che sta per
              <strong>"Protezione Animali Natura-Ente Provinciale Protezione Animali e
                Ambiente"</strong>, ha come scopo la tutela e la protezione degli animali
              di ogni genere e specie, nonché il loro habitat, attraverso la
              realizzazione e la gestione, anche per conto di terzi, di
              strutture atte al temporaneo ricovero di animali bisognosi di
              custodia o di cura, l'attività di sensibilizzazione ed
              educazione alle tematiche animaliste e ambientalistiche anche in
              collaborazione con ogni altra Associazione avente scopi uguali
              o affini, la cooperazione o la sollecitazione delle autorità
              competenti affinché siano osservate o poste in atto le norme
              nazionali e provinciali in materia di protezione degli animali
              e dell'ambiente.
